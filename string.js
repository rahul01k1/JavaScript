// ===============================
// JavaScript String Methods
// ===============================

let str = "  Hello World  ";
let text = "apple,banana,apple";
let name = "Vipul";

// --------------------------------------------------
// 1. length
// --------------------------------------------------
console.log(str.length);                    // 15

// --------------------------------------------------
// 2. charAt()
// --------------------------------------------------
console.log(str.charAt(2));                 // H

// --------------------------------------------------
// 3. at()
// --------------------------------------------------
console.log(str.at(-1));                    // " "

// --------------------------------------------------
// 4. charCodeAt()
// --------------------------------------------------
console.log("A".charCodeAt(0));             // 65

// --------------------------------------------------
// 5. codePointAt()
// --------------------------------------------------
console.log("😀".codePointAt(0));           // Unicode code point

// --------------------------------------------------
// 6. concat()
// --------------------------------------------------
console.log("Hello".concat(" ", "World"));  // Hello World

// --------------------------------------------------
// 7. includes()
// --------------------------------------------------
console.log(str.includes("World"));         // true

// --------------------------------------------------
// 8. startsWith()
// --------------------------------------------------
console.log(str.trim().startsWith("Hello")); // true

// --------------------------------------------------
// 9. endsWith()
// --------------------------------------------------
console.log(str.trim().endsWith("World"));   // true

// --------------------------------------------------
// 10. indexOf()
// --------------------------------------------------
console.log(str.indexOf("o"));              // First occurrence

// --------------------------------------------------
// 11. lastIndexOf()
// --------------------------------------------------
console.log(str.lastIndexOf("o"));          // Last occurrence

// --------------------------------------------------
// 12. search()
// --------------------------------------------------
console.log(str.search("World"));           // Index

// --------------------------------------------------
// 13. match()
// --------------------------------------------------
console.log("abc123".match(/\d+/));         // ["123"]

// --------------------------------------------------
// 14. matchAll()
// --------------------------------------------------
console.log([...text.matchAll(/apple/g)]);

// --------------------------------------------------
// 15. replace()
// --------------------------------------------------
console.log(str.replace("World","JavaScript"));

// --------------------------------------------------
// 16. replaceAll()
// --------------------------------------------------

console.log(text.replaceAll("apple","orange"));

// --------------------------------------------------
// 17. slice()
// --------------------------------------------------
console.log(str.slice(2,7));               // Hello

// --------------------------------------------------
// 18. substring()
// --------------------------------------------------
console.log(str.substring(2,7));           // Hello

// --------------------------------------------------
// 19. substr()  (Deprecated)
// --------------------------------------------------

console.log(str.substr(2,5));

// --------------------------------------------------
// 20. split()
// --------------------------------------------------
console.log(text.split(","));

// --------------------------------------------------
// 21. toUpperCase()
// --------------------------------------------------
console.log(str.toUpperCase());

// --------------------------------------------------
// 22. toLowerCase()
// --------------------------------------------------
console.log(str.toLowerCase());

// --------------------------------------------------
// 23. trim()
// --------------------------------------------------
console.log(str.trim());

// --------------------------------------------------
// 24. trimStart()
// --------------------------------------------------
console.log(str.trimStart());

// --------------------------------------------------
// 25. trimEnd()
// --------------------------------------------------
console.log(str.trimEnd());

// --------------------------------------------------
// 26. repeat()
// --------------------------------------------------
console.log("Hi ".repeat(3));

// --------------------------------------------------
// 27. padStart()
// --------------------------------------------------
console.log("5".padStart(3,"0"));          // 005

// --------------------------------------------------
// 28. padEnd()
// --------------------------------------------------
console.log("5".padEnd(3,"0"));            // 500

// --------------------------------------------------
// 29. valueOf()
// --------------------------------------------------
let s = new String("Hello");
console.log(s.valueOf());

// --------------------------------------------------
// 30. toString()
// --------------------------------------------------
console.log(s.toString());

// --------------------------------------------------
// 31. localeCompare()
// --------------------------------------------------
console.log("apple".localeCompare("banana"));
// -1 => before
// 0  => equal
// 1  => after

// --------------------------------------------------
// 32. normalize()
// --------------------------------------------------
console.log("é".normalize());

// --------------------------------------------------
// 33. isWellFormed() (Modern)
// --------------------------------------------------
if (String.prototype.isWellFormed) {
    console.log("Hello".isWellFormed());
}

// --------------------------------------------------
// 34. toWellFormed() (Modern)
// --------------------------------------------------
if (String.prototype.toWellFormed) {
    console.log("Hello".toWellFormed());
}

// --------------------------------------------------
// Template Literals
// --------------------------------------------------
console.log(`Hello ${name}`);

// --------------------------------------------------
// Escape Characters
// --------------------------------------------------
console.log("Hello\nWorld");
console.log("Hello\tWorld");
console.log("Hello\\World");
console.log("He said \"Hi\"");
